package com.example.autocamperrental;

public class Season {
    double highSeason=50;
    double lowSeason=-50;

    public double getHighSeason() {
        return highSeason;
    }

    public void setHighSeason(double highSeason) {
        this.highSeason = highSeason;
    }

    public double getLowSeason() {
        return lowSeason;
    }

    public void setLowSeason(double lowSeason) {
        this.lowSeason = lowSeason;
    }
}
